//: ## 代码补全
//: Xcode会自动跟踪你所定义过名字。因此，当你开始写代码的时候，就会看到一个名字的推荐列表。这称为代码补全（autocompletion)。
//: 
//: 代码补全功能可以帮助你更快地编写代码，并减少拼写错误。你输入的内容越多，代码补全的列表就越接近你要查找的内容。
//: 
//: 每当代码补全的菜单出现时，你可以:
//: - 继续输入以缩小列表的推荐范围。
//: - 使用向上和向下键来上下移动列表。
//: - 按回车键让Xcode填入所选的内容。
//:
//: ![Autocompletion Popup Menu](autocomplete.png)
//: 在列表中，你可能会看到你没有定义的名字。目前让我们暂时先忽略这些内容。
//: 
//: 作为下一页练习的一部分，你将输入一些名字，并在实际操作中了解代码补全功能。
