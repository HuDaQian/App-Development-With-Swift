//: 在Swift中，名字的官方叫法是标识符（identifier）。你通常会在各种各样错误消息中看到标识符。程序员平时使用非正式的词“名称”。本课程将同时使用这两种叫法，因此你后面会经常看到它们。
//: 
//: 常量的名字以小写字母开头。如下面的例子，它们可以包含几个组合在一起的字母（中间没有空格）。如果它们之间有一个空格，Swift会认为每个单词是一个独立的标识符。
//: 
//: 为了更方便地阅读带有多个单词的标识符，第一个单词后面的每个单词的首字母都应该大写。这种写法称为驼峰命名法，因为名字中间的大写字母看起来有点像骆驼的驼峰。 🐫
