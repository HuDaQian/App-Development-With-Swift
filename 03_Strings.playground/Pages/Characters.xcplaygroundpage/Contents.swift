//: ## 字符
//: 当然，文本不仅仅只包含字母: 还有标点符号，数字，符号，甚至空格。所有这些东西都称为字符（characters），字符串是由字符组成的。
//: 
//: 字母是一种字符:
//: ## Aa Bb Cc Dd Ee
//: 标点也是一种字符:
//: ## ！ ？ 。 ，; :
//: 数字:
//: ## 1 2 3 4 5 6 7 8 9 0
//: 符号: 
//: ## # @ $ & © π * + - /
//: 即使是不可见的字符，如空格和制表符:\
//: （你不能看到他们，但他们就在那里。）
//: ## `                         `
//:
//:
//: 目前为止你已经知道什么是字符串了。那么接下来，让我们一起动手写一些字符串。\
