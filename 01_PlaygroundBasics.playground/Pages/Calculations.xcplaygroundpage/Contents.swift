//: ## 计算器
//: 我们可以把playground当做计算器来用。输入算术问题后，计算的结果就会显示在results sidebar中。
//:
//: 你可以使用加号 `+` 和减号 `-` ，比如下面这样:
34 + 56 + 230

1000 - 300

//: 乘法，使用星号 `*` :
3 * 100
//: 除法，使用斜杠 `/` :
24 / 8
//: 分组，使用小括号 `( )` :
(300 + 500 + 400) / 3
