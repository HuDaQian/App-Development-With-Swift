//: (而且可能比你想象中的更经常。)
//:
//: 当你开始写代码，开始开发app的时候，放错误是难免的。
//:
//: 乖乖，不要慌
//:
//: 在写代码的时候放错误是非常正常的。\
//: 不用希望所有的代码都可以在一开始的时候正常运行。
//:
//: 在出现问题时进行故障排除，是程序员日常工作的一部分。
//:
//: 第一步就是不要害怕，深吸一口气。
//:
//: 乖乖，不要慌
//:
//: 一个错误并不意味着你失败了，或者你是一个差劲的程序员。\
//: 它只是意味着在代码中有一个问题，你需要去修复。
//:
//: 这个只是日常中的一小部分。
